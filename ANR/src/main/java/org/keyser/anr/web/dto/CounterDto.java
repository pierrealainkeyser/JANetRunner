package org.keyser.anr.web.dto;

public class CounterDto {

	private Integer heap;

	private Integer stack;

	private Integer grip;

	private Integer rd;

	private Integer hq;

	private Integer archives;

	public Integer getHeap() {
		return heap;
	}

	public void setHeap(Integer heap) {
		this.heap = heap;
	}

	public Integer getStack() {
		return stack;
	}

	public void setStack(Integer stack) {
		this.stack = stack;
	}

	public Integer getGrip() {
		return grip;
	}

	public void setGrip(Integer grip) {
		this.grip = grip;
	}

	public Integer getRd() {
		return rd;
	}

	public void setRd(Integer rd) {
		this.rd = rd;
	}

	public Integer getHq() {
		return hq;
	}

	public void setHq(Integer hq) {
		this.hq = hq;
	}

	public Integer getArchives() {
		return archives;
	}

	public void setArchives(Integer archives) {
		this.archives = archives;
	}
}
